#include<ini.h>

/******************************************************************************
 *                                                                            *
 *   C O N S T A N T S                                                        *
 *                                                                            *
 ******************************************************************************/
#define TOTAL_TESTS 1

/******************************************************************************
 *                                                                            *
 *   T Y P E S                                                                *
 *                                                                            *
 ******************************************************************************/
typedef enum testResultINI { INI_SUCC, INI_FAIL } testResultINI_t;

/******************************************************************************
 *                                                                            *
 *   G L O B A L S                                                            *
 *                                                                            *
 ******************************************************************************/
ini_t *ini;

/******************************************************************************
 *                                                                            *
 *   T E S T S                                                                *
 *                                                                            *
 ******************************************************************************/
testResultINI_t testMakeINI() {
  fprintf(stdout, "FAIL: testMakeINI\n");
  return INI_FAIL; 
}

/******************************************************************************
 *                                                                            *
 *   M A I N                                                                  *
 *                                                                            *
 ******************************************************************************/
testResultINI_t (*TESTS[TOTAL_TESTS])() = {
  testMakeINI,
};

int main() {

  int test, passes, fails;
  for(test = passes = fails = 0; test < TOTAL_TESTS; test++) {
    ((*TESTS[test])() == INI_SUCC) ? passes++ : fails++;
  }

  fprintf(stdout, "\n--------------------\n"
                    "Passes:           %i\n"
                    "Fails:            %i\n"
                    "Total Tests:      %i\n", passes, fails, passes+fails);

  return 0;  
}
